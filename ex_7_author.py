# Ex.7: Sum fo the First n Positive Integers (p.5). Author version.

n = int(input("Enter a positive integer: "))

# Compute the sum.
sm = n * (n + 1) / 2

# Display the result
print("The sum of the first", n, "positive integers is", sm)

